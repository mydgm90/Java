package ch02;

public class ex04Float {
    public static void main(String[] args) {
        float f1 = 3.14f;
        double d1 = 3.14;
        System.out.println(f1+","+d1);

        double d2 = 3.14e+2;
        double d3 = 3.14e-2;
        System.out.println(d2);
        System.out.println(d3);

        float f2 = 0.123451234512345f;
        double d4 = 0.123451234512345;
        System.out.println(f2);
        System.out.println(d4);
    }
}
